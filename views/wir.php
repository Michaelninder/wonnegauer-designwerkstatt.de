    <h2>Wir</h2>
    <p>Wir, Brigitte und Wolfgang Ternis, haben mit der Wonnegauer Designwerkstatt eine Möglichkeit geschaffen, unseren Interessen ein Forum zu geben. Unser Studium zum Dipl.Designer (FH) absolvierten wir in Mainz, im Verlauf dessen wir uns auch fanden. Im Rahmen des Studiums wurden wir unter anderem in den Fächern Plastisches Gestalten und Freihandzeichnen auch von Bildhauer Prof. Eberhard Linke ausgebildet.</p>
<div>
    <h3>Brigitte</h3>
    <p>
        Nach fundierter kaufmännischer und betriebswirtschaftlicher Ausbildung und Berufstätigkeit entwickelte sich das Interesse an Kunst und Architektur sowie eigenem gestalterischem Schaffen immer stärker.
        So begann ich das Studium der Innenarchitektur in Mainz, bei dem ich meinen Mann kennenlernte.
        Wir arbeiteten zusammen freiberuflich im Bereich Planung von Messeständen und Möbelhäusern.
        Durch familiäre- und berufliche Entwicklungen kam ich in Kontakt mit alternativen Heil- und
        Lebensansätzen. Verschiedene Ausbildungen in NLP, Theta-Healing u.v. mehr waren die logische Folge.
        Mit der Arbeit als Gästeführerin ergab sich auch die Ausbildung zur Kultur-und Weinbotschafterin und ganz neu die Ausbildung zur Fahrrad-Gästeführerin.
    </p>
    <h3>Wolfgang</h3>
    <p>
        Schon in früher Jugend war für mich die "Räumlichkeit" ein faszinierendes Betätigungsfeld.
        Später entstanden Plastiken und Produkte aus unterschiedlichen Materialien.
        Die Teilnahme an verschiedenen Präsentations-foren, wie der 1. Designbörse Stuttgart, der Internationalen Bienale der Wohnkreativität in Kortrijk (Belgien) erweiterten meine gestalterischen Perspektiven.
        Veröffentlichungen in internationalen Zeitschriften wie "md Möbeldesign" und "Gold und Silber, Uhren und Schmuck" zeigten ein positives Echo.
        Regionales Interesse dokumentieren verschiedene Berichte in der Wormser Zeitung und im offenen Kanal Worms.
        Durch die Gründung unserer Familie traten die kreativen Arbeiten zunächst etwas in den Hintergrund.
        Seit 2009 bin ich wieder verstärkt gestalterisch tätig und fertige vorwiegend Bilder in Acryltechnik.
        Natürlich ist für mich die "Räumlichkeit" immer noch der prägende Gestaltungsansatz, wobei sich auch gesellschaftskritische Themen ihren Platz erobern.
    </p>
</div>
<p>Mit der Eröffnung unseres Ateliers am 04.August 2013 sind etliche der Werke zugänglich.</p>

